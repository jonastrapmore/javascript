// Array.push - voegt achteraan een element toe.
const kleuren = ["rood", "geel"];
console.log(kleuren);

kleuren.push("blauw");
console.log('Push 1 extra: ', kleuren); // ["rood","geel","blauw"]
kleuren.push("wit","zwart");
console.log('Push 2 extra: ', kleuren); // ["rood","geel","blauw","wit","zwart"]
